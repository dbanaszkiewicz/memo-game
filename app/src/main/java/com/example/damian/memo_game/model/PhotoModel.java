package com.example.damian.memo_game.model;

public class PhotoModel {
    private String name, url;

    public PhotoModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
